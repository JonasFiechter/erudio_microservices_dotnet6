namespace crud_person.Model
{
    public class Person
    {
        public long Id;
        public string FirstName;
        public string LastName;
        public string Address;
        public string Gender;

        public Person()
        {
            this.Id = Id;
            this.FirstName = FirstName;
            this.LastName = LastName;
            this.Address = Address;
            this.Gender = Gender;
        }
    }
}
